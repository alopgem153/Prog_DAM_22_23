package Funciones;

public class MatematicasBasicas {
    

    /**
     * Suma 2 enteros
     * @param a
     * @param b
     * @return
     */
    static int suma(int a, int b)
    {
        return a + b;
    }

    /**
     * Suma 3 enteros
     * @param a
     * @param b
     * @param c
     * @return
     */
    static int suma(int a, int b, int c)
    {
        return a + b + c;
    }
}
